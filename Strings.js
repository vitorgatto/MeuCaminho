const escola = "Cod3r"

console.log (escola.charAt(4)) /* charAt(x) = imprime a letra que esta na 
                                posição X da string. Neste caso, R. 
                                COMEÇANDO COM 0.
*/
console.log(escola.charCodeAt(3)) // charCodeAt = da o codigo do caracter
console.log(escola.indexOf('3')) /* indexOf = indica o indice do lugar que esta a 
                                    letra dada
*/
console.log(escola.substring(1)) /* substring = imprime os caracteres
                                    subsequentes ao valor dado (1) */
console.log("Escola".concat(escola).concat("!")) /* concat = combina o texto
                                    de duas strings e retorna uma nova string.                               
*/
console.log ('Ana, Maria, Pedro'.split(',')) 
